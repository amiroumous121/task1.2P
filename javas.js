let str = "Hello, World!";

console.log(str.length); // Outputs the length of the string
console.log(str.split(",")); // Splits the string into an array at each comma
console.log(str.replace("Hello", "Hi")); // Replaces "Hello" with "Hi" in the string
console.log(str.toUpperCase()); // Converts the string to uppercase
console.log(str.toLowerCase()); // Converts the string to lowercase
let numArray = [1, 2, 3, 4, 5];
let strArray = ["Hello", "World", "!"];

console.log(numArray.length); // Outputs the length of the number array
console.log(numArray.sort()); // Sorts the number array
console.log(strArray.join(" ")); // Joins the string array into a single string with spaces between each item
console.log(strArray.pop()); // Removes the last item of the string array and outputs it
console.log(strArray.push("Hello again")); // Adds "Hello again" to the end of the string array and outputs the new length
